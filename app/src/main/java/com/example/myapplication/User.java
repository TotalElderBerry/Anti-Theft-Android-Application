package com.example.myapplication;

public class User {
    public String Email, FirstName, LastName, Username, ContactNo, Brand;

    public User(String email, String firstName, String lastName, String username, String contactNo, String brand){
        this.Email = email;
        this.FirstName = firstName;
        this.LastName = lastName;
        this.Username = username;
        this.ContactNo = contactNo;
        this.Brand = brand;
    }
}
